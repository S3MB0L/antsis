/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{
char a=97;

  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
		if(HAL_UART_Receive_DMA(&huart1,(uint8_t *)&rx_byte,1)==0){
			switch(rx_byte){
				case 0: a=Buttons_GetState(); 
							  HAL_UART_Transmit_DMA(&huart1,(uint8_t *)&a,1);
								break;
			
				case 1: HAL_UART_Receive_DMA(&huart2,(uint8_t *)&rx_byte,1);
								a=rx_byte;
								HAL_UART_Transmit_DMA(&huart1,(uint8_t *)&a,1);
								break;
			}
		}
				
		HAL_UART_Transmit_DMA(&huart1,(uint8_t *)&a,1);
    osDelay(100);
  }
  /* USER CODE END 5 */ 
}
	

